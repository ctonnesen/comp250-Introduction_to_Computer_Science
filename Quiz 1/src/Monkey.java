public class Monkey {

    int age; 

    private String name;

 

    public Monkey(String name, int age) {

        this.name = name;

        this.age = age;

    }

    public void setName(String name) {

        this.name = name; 

    }

    public void setAge(int age) {

        this.age = age; 

    }

}